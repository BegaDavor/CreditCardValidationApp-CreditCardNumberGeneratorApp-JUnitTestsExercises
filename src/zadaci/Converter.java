package zadaci;

public class Converter {
	
	public double kgToFunt(double num) {
		
		return num * 2.4419;
		
	}
	
	public double funtToKg(double num) {
		return num / 2.4419;
	}
	
	public double cmToInch(double num) {
		return num * 0.393700787;
	}
	
	public double inchToCm(double num) {
		return num / 0.393700787;
	}
	
	public double cToF(double num) {
		return num * 33.8;
	}
	
	public double fToC(double num) {
		return num / 33.8;
	}

}
